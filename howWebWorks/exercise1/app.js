// - What is a query string?
//A query string is a part of a uniform resource locator (URL) that assigns values to specified parameters.

// - What are two HTTP verbs and how are they different?
//GET and POST

// - What is an HTTP request?
//An HTTP request is a packet of information sent by a client to a server in order to make a request for information.

// - What is an HTTP response?
//An HTTP response is the packet of information sent by a server to a client in response to an earlier request made by the client.

// - What is an HTTP header? Give a couple examples of request and response headers you have seen.
//HTTP headers let the client and the server pass additional information with an HTTP request or response. 
//An HTTP header consists of its case-insensitive name followed by a colon (:), then by its value. Whitespace before the value is ignored.

